    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String nome_pais = jTextField1.getText();
            Pais adicionar = criaPais(nome_pais);
            if (adicionar != null) {
                Document doc = XMLJDomFunctions.lerDocumentoXML("paises.xml");
                doc = ModeloXML.adicionaPais(adicionar, doc);
                XMLJDomFunctions.escreverDocumentoParaFicheiro(doc, "paises.xml");

                Document doc1 = XMLJDomFunctions.lerDocumentoXML("factos.xml");
                doc1 = ModeloXML.adicionaFactos(adicionar, doc1);
                XMLJDomFunctions.escreverDocumentoParaFicheiro(doc1, "factos.xml");

                jDialog1.setVisible(false);
                JOptionPane.showMessageDialog(this,
                        "País e Factos adicionados com sucesso!",
                        "Informação",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                jDialog1.setVisible(false);
                JOptionPane.showMessageDialog(this,
                        "País e Factos já se encontram adicionados!",
                        "Informação",
                        JOptionPane.INFORMATION_MESSAGE);
            }

            jTextField1.setText("");
        } catch (IOException | SaxonApiException ex) {
            Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

