    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        String nome_pais = jTextField8.getText();

        if ("Pesquisar os factos de um determinado país".equals(jDialog6.getTitle())) {
            String xp = "//pais[@nome='" + nome_pais + "']";
            XdmValue res = null;
            try {
                res = XPathFunctions.executaXpath(xp, "factos.xml");
            } catch (SaxonApiException ex) {
                Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

            String s = XPathFunctions.listaResultado(res);
            if (res == null) {
                jTextArea5.setText("Ficheiro XML não existe");
            } else if (res.size() == 0) {
                jTextArea5.setText("Sem resultados");
            } else {
                jTextArea5.setText(s);
            }
        } else if ("Pesquisar pelo nome do país e mostrar a informação relevante".equals(jDialog6.getTitle())) {
            String xp = "//pais[@nome='" + nome_pais + "']/concat(@nome, ', ', continente, ', ', presidente)";
            XdmValue res = null;
            try {
                res = XPathFunctions.executaXpath(xp, "paises.xml");
            } catch (SaxonApiException ex) {
                Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

            String s = XPathFunctions.listaResultado(res);
            if (res == null) {
                jTextArea5.setText("Ficheiro XML não existe");
            } else if (res.size() == 0) {
                jTextArea5.setText("Sem resultados");
            } else {
                jTextArea5.setText(s);
            }
        }

        jTextField8.setText("");
    }//GEN-LAST:event_jButton11ActionPerformed

